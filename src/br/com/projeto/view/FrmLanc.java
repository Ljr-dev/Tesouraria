    private void btsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsalvarActionPerformed

        // botao salvar
        String recebida = txtdata.getText();

        String[] dataSeparada = recebida.split("/");

        LocalDate data = LocalDate.of(Integer.parseInt(dataSeparada[2]), Integer.parseInt(dataSeparada[1]), Integer.parseInt(dataSeparada[0]));

        Lanc obj = new Lanc();

        obj.setData(String.valueOf(data));
        obj.setDescricao(txtdescricao.getText());
        obj.setStatus_(txtstatus.getText());
        obj.setClassificacao(txtclassificacao.getText());
        obj.setDocumentacao_suporte(txtdocumentacao.getText());
        obj.setTipo(txttipo.getSelectedItem().toString());
        obj.setValor(Double.parseDouble(txtvalor.getText()));
        obj.setBanco(txtbanco.getText());

        LancDAO dao = new LancDAO();

        dao.cadastrarLanc(obj);

        new Utilitarios().LimpaTela(painellan);

    }//GEN-LAST:event_btsalvarActionPerformed

