    private void btnpesquisapordataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnpesquisapordataActionPerformed
        //pesquisar  por data
        try {
            //Receber as datas

            String tipo = "%" + txtpesquisa.getText() + "%";

            DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy");

            LocalDate data_inicio = LocalDate.parse(txtdatainicio.getText(), formato);
            LocalDate data_fim = LocalDate.parse(txtdatafim.getText(), formato);

            LocalDate data_inicioRec = LocalDate.parse(txtdatainicio.getText(), formato);
            LocalDate data_fimRec = LocalDate.parse(txtdatafim.getText(), formato);
            LocalDate data_inicioDes = LocalDate.parse(txtdatainicio.getText(), formato);
            LocalDate data_fimDes = LocalDate.parse(txtdatafim.getText(), formato);

            LancDAO dao = new LancDAO();

            List<Lanc> lista = dao.listarLancPorPeriodo(data_inicio, data_fim, tipo);
            List<Lanc> lista1 = dao.listarLancTotal(data_inicioRec, data_fimRec, data_inicioDes, data_fimDes);
            List<Lanc> lista2 = dao.listarRecData(data_inicioRec, data_fimRec);
            List<Lanc> lista3 = dao.listarDesData(data_inicioDes, data_fimDes);

            DefaultTableModel dados = (DefaultTableModel) tabelaLanc.getModel();
            DefaultTableModel dados1 = (DefaultTableModel) tabelaSaldo.getModel();
            DefaultTableModel dados2 = (DefaultTableModel) tabelaReceita.getModel();
            DefaultTableModel dados3 = (DefaultTableModel) tabelaDespesa.getModel();

            dados.setNumRows(0);
            dados1.setNumRows(0);
            dados2.setNumRows(0);
            dados3.setNumRows(0);

            for (Lanc c : lista) {
                dados.addRow(new Object[]{
                    c.getId(),
                    c.getData(),
                    c.getDescricao(),
                    c.getStatus_(),
                    c.getClassificacao(),
                    c.getDocumentacao_suporte(),
                    c.getTipo(),
                    c.getValor(),
                    c.getBanco(),});
            }

            for (Lanc c : lista1) {
                dados1.addRow(new Object[]{
                    c.getSaldo(),});
            }

            for (Lanc d : lista2) {

                dados2.addRow(new Object[]{
                    d.getValor()

                });
            }
            for (Lanc d : lista3) {

                dados3.addRow(new Object[]{
                    d.getValor()

                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Digite duas datas como intervalo!");

        }


    }//GEN-LAST:event_btnpesquisapordataActionPerformed

